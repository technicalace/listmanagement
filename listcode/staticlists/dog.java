package listcode.staticlists;

public class dog {
  
    private String name, desc;
    private int age; 
    // dog setters 
    public void setName(String name) {
        this.name = name; 
    }

    public void setDesc(String desc) {
        this.desc = desc; 
    }

    public void setAge(int age) {
        this.age = age; 
    }
    // dog getters 
    public String getName() {
        return name; 
    }

    public String getDesc() {
        return desc;
    }

    public int getAge() {
        return age;
    }
}
